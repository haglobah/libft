#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*arr;
	int		i;

	arr = (char *) malloc(sizeof(char) * ft_strlen(s) + 1);
	i = 0;
	while (s[i])
	{
		arr[i] = s[i];
		i++;
	}
	arr[i] = '\0';
	return (arr);
}
